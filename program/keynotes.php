		<h3 class="no-margin"><strong style="font-size: 16px;">Title: The Social Turn and its big enemy: a leap forward</strong></h3>
		<p style="text-align: justify;">The socio-political movement called the Social Turn in mathematics education has alerted people to the use of mathematics as a factor of social exclusion. The political efficacy of the movement as a driver of change relies on the degree of indignation that it is able to provoke. We suggest that, instead of a having long discussion to define a minimal positive agenda on which we all could agree, perhaps we can set a maximal negative agenda by eliciting a discourse that we all would spontaneous and promptly reject. In this article, we create a character that would utter such discourse, and call it the Big Enemy, paraphrasing George Orwell. It is an entity incapable of showing any indignation when confronted with the charges of the Social Turn; on the contrary, it maintains that school is as it should be. We present a hypothetical dialogue between BE and Mrs. Smith, a teacher and researcher devoted to the directives of the Social Turn. Our argument is based on economic principles and Lacanian psychoanalysis.
		</p>
<h3 class="no-margin"><strong style="font-size: 16px;">Biographical note: </strong></h3>
