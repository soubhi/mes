		<h3 class="no-margin"><strong style="font-size: 16px;">Title: Different Ways of Knowing<br><span style="font-size:14px">- Styles of Argument in Greek and Indian Mathematical Traditions</span></strong></h3>
		<p style="text-align: justify;">In this talk, I propose to examine critically the ways in which different mathematical traditions of the past have been characterised by historians of mathematics. A litmus test of a valid mathematical practice today is 'proof' and a number of criticisms have been levied against certain traditions because of the perceived absence or lack of rigour in their proof procedures (seen today as the litmus test of whether we are "doing" real mathematics or doing it well). By taking specific examples, I propose to examine the validity of these criticisms and indicate the relevance of alternative proof traditions today. Further, such an examination is often compounded by certain deeply seated historiographic bias in Western scholarship, a complex product of colonialism and hellenophilia. While there is no intention to rehearse the argument relating to the nature and origin of this bias, it is important that this bias be recognised and countered in any attempt to “recover and reclaim the world history of science”.
		</p>
