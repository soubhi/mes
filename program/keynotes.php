		<h3 class="no-margin"><strong style="font-size: 16px;" > Title: Mathematics education: Its role in the revitalisation of Indigenous languages and cultures</strong></h3>
		<p style="text-align: justify;"> Scholars writing from social justice perspectives have previously identified a range of issues associated with the imposition of Western mathematics on Indigenous groups. Collectively, some of the major concerns are: the claim that mathematics is culture free, when in fact mathematics has been one of the most powerful weapons in the imposition of Western culture (Bishop, 1990, 1991); that the political dimension of mathematics is often ignored in regard to marginalised cultures which raises ethical and moral issues (D’Ambrosio, 1985); and that education systems often assume that students from minority groups should be taught subjects such as mathematics only through a majority or dominant language (Barwell, 2003). This is because the language used in schools, as in wider society, is closely bound up with issues of “access, power and dominance” (Barwell, 2003, p. 2). <br>
Paradoxically, schooling in Aotearoa/NZ and mathematics education has played a pivotal role in first supporting te reo Māori loss and then in the modern era, its revitalisation. Māori mathematics educators have utilised mathematics as a vehicle to support the revitalisation of the language. This has been aided by the high status of mathematics in the school curriculum in Aotearoa/NZ generally. I report on our various first-hand experiences, initially at the grassroots level of teaching and then the national level, developing lexicon, curricula and teacher capacity to teach mathematics in the medium of Māori. Drawing on the work of Nancy Fraser (1995, 2003, 2005), I critique these major mathematics education developments from a social justice perspective.
		</p>
<h3 class="no-margin"><strong style="font-size: 16px;">Biographical note: </strong></h3>
