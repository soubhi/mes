		<p style= "text-align: justify;">The relationship between humans, mathematics, and the planet has been one steeped too long in domination and destruction. What are appropriate responses to reverse such a relationship? How do we do work now (inside and outside of schools) that will reverberate and touch the lives of future generations? Drawing upon Indigenous worldviews to reconceptualize what mathematics is and how it is practiced, I argue for a movement against objects, truths, and knowledge towards a way of being in the world that is guided by first principles--mathematx. This shift from thinking of mathematics as a noun to mathematx as a verb holds potential for honouring our connections with each other as human and other-than-human persons, for balancing problem solving with joy, and for maintaining critical bifocality at the local and global level. </p>
<h3 class="no-margin"><strong style="font-size: 16px;">Biographical note: </strong> </h3>
